listem = [1, 2.0, [3, 4], "merhaba"]
for i in listem:
    if type(i) == int:    # isinstance(i,int):
        print(i, "\t\tben olmak bir int ")
    if type(i) == float:  # isinstance(i,float):
        print(i, "\t\tben olmak bir float ")
    if type(i) == str:    # isinstance(i,str):
        print(i, "\tben olmak bir str ")
    if type(i) == list:  # isinstance(i,list):
        print(i, "\t\tben olmak bir list ")

